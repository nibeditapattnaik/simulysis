		<h3>We're a team of experts in the field of CAE.</h3>
		<p>
			We are a start up from Indian Institute of Technology Madras (IITM), driven by our love for problem solving and aspiration to contribute to the domain of technology.
		</p>
		<p>
			We provide training in the field of Computer Aided Engineering (CAE) for working professionals and students. Finite element analysis (FEA) and Computational Fluid Dynamics (CFD) are our expertise. Our courses are specially designed to develop a conceptual and practical understanding. We use various open source tools like Salome, Code-Saturne, OpenFoam, Code-Aster, Elmer, Python, Scilab, Octave for CFD & FEA simulations.
		</p>
		<h4>Learning CAE is much more than learning a particular commercial software !!!</h4>
		<p>
			Many a time, learning such wonderful and versatile techniques are confused with handling of certain commercial software. While any software with well-designed and attractive user interface creates an euphoria about its capability and sophistication, but many end up using them like a black-box. Even people with good experience and expertise in certain software, end-up following a trial and error approach due to lack of understanding of the basics of the technique. So, it is important for any practising (as well as aspiring) engineers and researchers to have a complete end to end understanding of the methods.
		</p>
		<p>This includes the following</p>
    <ul>
      <li>Pre-processing: This includes all the steps involved in transforming the actual governing equation of the problem to a numerical formulation and applying them over a discretized domain (meshing) along with appropriate boundary and initial conditions. The problem over the continuous domain is mapped to finite number points.</li>
      <li>Solving: This involves finding the solution of the numerical formulation with desired accuracy level in an iterative manner by using appropriate solving technique. For example, solving techniques for linear and non-linear problem ought to be different. So understanding the mathematical model always helps in choosing appropriate solver.</li>
      <li>Post-processing: In this step the solution obtained at finite number of points is further processed and interpolated to get the approximate solution over the entire domain.</li>
    </ul>
    <h4>Course Objective</h4>
    <p>So the objective of learning CAE techniques needs to be learning the problem solving approach rather than particular software. Someone with good idea of the basics of methods, can always adapt any software quickly, as irrespective of how the user interface looks in different software the core remains same.</p>
    <p>Our courses are designed with the following objectives.</p>
    <ul>
      <li>Help engineering graduates to acquire more practical knowledge of Design and Analysis.</li>
      <li>Provide In depth knowledge in FEM/FEA/CFD practices.</li>
      <li>To spread R&D activities at academic institutes.</li>
    </ul>
